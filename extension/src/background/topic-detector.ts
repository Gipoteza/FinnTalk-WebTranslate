// Определение тематики страницы и формирование системного промпта

import type { Topic, TranslationStyle } from '../types'

export const VALID_TOPICS: Topic[] = [
  'медицина',
  'юриспруденция',
  'финансы',
  'техника',
  'IT',
  'бытовая',
  'деловая',
  'разговорная',
]

const TOPIC_MAP: Record<string, Topic> = {
  'медицина': 'медицина',
  'юриспруденция': 'юриспруденция',
  'финансы': 'финансы',
  'техника': 'техника',
  'it': 'IT',
  'IT': 'IT',
  'бытовая': 'бытовая',
  'деловая': 'деловая',
  'разговорная': 'разговорная',
}

export function normalizeTopic(response: string): Topic {
  const trimmed = response.trim()
  const lower = trimmed.toLowerCase()

  // Проверяем точное совпадение (case-insensitive)
  for (const [key, value] of Object.entries(TOPIC_MAP)) {
    if (key.toLowerCase() === lower) {
      return value
    }
  }

  return 'бытовая'
}

